// Задача с пирамидой

//      *
//     ***
//    *****
//   *******
//  *********
// ***********

const lines = 6;
let result = "";

for (let i = 0; i < lines; i++) {
  // Добавляем пробелы
  for (let j = 0; j < lines - i - 1; j++) {
    result += " ";
  }

  // Добавляем звёздочки
  for (let k = 0; k < 2 * i + 1; k++) {
    result += "*";
  }

  // Переход на новую строку
  result += "\n";
}
